package com.tcs.pim.domain;

public class SubProductCategoryGroup {
	public String id;
	public String subProductCategoryGroup;
	public String order;
	public String country;
	public String language;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSubProductCategoryGroup() {
		return subProductCategoryGroup;
	}
	public void setSubProductCategoryGroup(String subProductCategoryGroup) {
		this.subProductCategoryGroup = subProductCategoryGroup;
	}
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}

}
