package com.tcs.pim.domain;

public class UsesAndApplications {
	private String id;
	private String usesAndApplication;
	private String order;
	private String country;
	private String language;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUsesAndApplication() {
		return usesAndApplication;
	}
	public void setUsesAndApplication(String usesAndApplication) {
		this.usesAndApplication = usesAndApplication;
	}
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	
}
