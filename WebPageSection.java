package com.tcs.pim.domain;

public class WebPageSection {
	private String webPageSectionId;
	private String language;
	private String webPageSectionName;
	private String webPageSectionText;

	public String getWebPageSectionId() {
		return webPageSectionId;
	}

	public void setWebPageSectionId(String webPageSectionId) {
		this.webPageSectionId = webPageSectionId;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getWebPageSectionName() {
		return webPageSectionName;
	}

	public void setWebPageSectionName(String webPageSectionName) {
		this.webPageSectionName = webPageSectionName;
	}

	public String getWebPageSectionText() {
		return webPageSectionText;
	}

	public void setWebPageSectionText(String webPageSectionText) {
		this.webPageSectionText = webPageSectionText;
	}

}
