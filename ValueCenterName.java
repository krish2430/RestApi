package com.tcs.pim.domain;

public class ValueCenterName {

	private String country;
	private String language;
	private String valueCenterName;
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public String getValueCenterName() {
		return valueCenterName;
	}
	public void setValueCenterName(String valueCenterName) {
		this.valueCenterName = valueCenterName;
	}
}
